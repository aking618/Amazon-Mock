class Product {
  final String name;
  final String quantity;
  final int rating;
  final int reviewCount;
  final double price;
  final bool prime;
  final String deliveryType;
  final String deliveryDate;

  Product(
    this.name,
    this.quantity,
    this.rating,
    this.reviewCount,
    this.price,
    this.prime,
    this.deliveryType,
    this.deliveryDate,
  );
}
